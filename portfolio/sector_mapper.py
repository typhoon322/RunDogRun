def get_sector_map() -> dict[str, str]:
    """返回完整映射 (本地 + 缓存)"""
    # 1. 优先读缓存
    if os.path.exists(CACHE_PATH):
        try:
            with open(CACHE_PATH, encoding="utf-8") as f:
                cached = json.load(f)
            merged = {**cached, **_LOCAL_MAP}  # 本地覆盖缓存
            return merged
        except Exception:
            pass

    # 2. 纯本地
    return dict(_LOCAL_MAP)


def lookup_sector(code: str) -> str:
    """查询单只股票行业"""
    m = get_sector_map()
    code = str(code).zfill(6)
    return m.get(code, "未知")


def build_and_cache():
    """从 AKShare 批量补全行业映射并缓存"""
    logger.info("正在构建行业映射...")
    try:
        import akshare as ak
    except ImportError:
        logger.warning("akshare 未安装, 仅使用本地映射")
        return

    # 获取所有行业板块
    try:
        boards = ak.stock_board_industry_name_em()
        boards = boards["板块名称"].tolist()
        logger.info(f"发现 {len(boards)} 个行业板块")
    except Exception:
        logger.warning("无法获取行业板块列表")
        return

    new_map = {}
    for i, board in enumerate(boards[:30]):  # 限制30个主要板块避免过多API调用
        try:
            df = ak.stock_board_industry_cons_em(symbol=board)
            for _, row in df.iterrows():
                code = str(row["代码"]).zfill(6)
                new_map[code] = board
            if (i + 1) % 10 == 0:
                logger.info(f"  进度: {i+1}/{min(30, len(boards))}")
        except Exception:
            continue

    # 合并到本地 → 缓存
    full_map = {**dict(_LOCAL_MAP), **new_map}
    Path(CACHE_PATH).parent.mkdir(parents=True, exist_ok=True)
    with open(CACHE_PATH, "w", encoding="utf-8") as f:
        json.dump(full_map, f, ensure_ascii=False, indent=2)

    logger.info(f"行业映射已缓存: {len(full_map)} 只 → {CACHE_PATH}")
    return full_map


if __name__ == "__main__":
    build_and_cache()
